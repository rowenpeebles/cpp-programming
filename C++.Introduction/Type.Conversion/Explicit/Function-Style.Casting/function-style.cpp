#include<iostream>

using namespace std;

int main() {

    // Initialising int variable
    int num_int = 26;

    // Declaring double variable
    double num_double;

    // Converting from int to double
    num_double = (double)num_int;

    cout << "num_int = " << num_int << endl;
    cout << "num_double = " << num_double << endl;

    return 0;

}
