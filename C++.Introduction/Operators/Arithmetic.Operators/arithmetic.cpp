#include <iostream>

using namespace std;

int main() {

    int a, b;

    a = 7;
    b = 2;

    // Printing the sum of a and b
    cout << "a + b = " << (a + b) << endl;

    // Printing the difference of a and b
    cout << "a - b = " << (a - b) << endl;

    // Printing the product of a and b
    cout << "a * b = " << (a * b) << endl;

    // Printing the division of a and b
    cout << "a / b = " << (a / b) << endl;

    // Printing the modulo of a by b
    cout << "a % b = " << (a % b) << endl;

    return 0;

}
